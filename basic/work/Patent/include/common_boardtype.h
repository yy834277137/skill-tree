/*
    版本:用32位长整型表示。高31～24位:主版本号(MAJOR);23～16:次版本号(MINOR);
    15～0:编译时间（15-12:年；11-8:月；7-0:日)
 */
#define MAKE_VERSION(_major, _minor, _year, _month, _day) \
    ( \
        ((_major) << 24) \
        | (((_minor) & 0xff) << 16) \
        | (((_year - 2000) & 0xf) << 12) \
        | (((_month) & 0xf) << 8) \
        | ((_day) & 0xff) \
    )

#define GET_VERSION_MAJOR(_ver) ((_ver) >> 24)
#define GET_VERSION_MINOR(_ver) (((_ver) >> 16) & 0xff)
#define GET_VERSION_YEAR(_ver)	((((_ver) >> 12) & 0xf) + 2000)
#define GET_VERSION_MONTH(_ver) (((_ver) >> 8) & 0xf)
#define GET_VERSION_DAY(_ver)	((_ver) & 0xff)

