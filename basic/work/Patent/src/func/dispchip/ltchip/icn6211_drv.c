/*******************************************************************************
* КЏЪ§Ућ  : ICN6211_HardReset
* Уш  Ъі  : ICN6211гВМўИДЮЛ
* Ъф  Шы  : 
* Ъф  Гі  : 
* ЗЕЛижЕ  : SAL_SOK : ГЩЙІ
*         SAL_FAIL : ЪЇАм
*******************************************************************************/
static INT32 ICN6211_HardReset(VOID)
{
    INT32 s32Fd =  -1;
    INT32 s32status = 0;
    DDM_resetSet  stResetPrm;

    s32Fd = open(DDM_DEV_RESET_NAME, O_RDWR);
    if(s32Fd < 0)
    {
        DISP_LOGE("open %s failed\n", DDM_DEV_RESET_NAME);
        return SAL_FAIL;
    }

    //icn6211
    memset((char *)&stResetPrm, 0, sizeof(DDM_resetSet));
    stResetPrm.type = DDM_RESET_TYPE_DISPLAY;      //ЯдЪОФЃПщ
    stResetPrm.subtype = DDM_DP_SUBTYPE_ICN6211;   //ЖўМЖзгРраЭЮЊicn6211ЯдЪОФЃПщ
    stResetPrm.num = 0;                            

    s32status = ioctl(s32Fd, DDM_IOC_RESET_OFFON, &stResetPrm);  //ЪЙгУioctlНгПкИДЮЛ
    close(s32Fd);
    if (s32status < 0)
    {
        DISP_LOGE("RESET ioctl reset ICN6211 failed fd %d %d!\n",s32Fd, s32status);
        return SAL_FAIL;
    }
    else
    {
        DISP_LOGI("ICN6211 reset ok!\n");
    }
    return SAL_SOK;

}

