#include "time_profile.h"

//该接口只用来初始化 CUDA 计时
#ifdef CUDA
/***********************************************************************************************************************
* 功  能：获取系统当前时间
* 参  数：IN/OUT:
*         time-----------------------------------当前时间
* 返回值：无
***********************************************************************************************************************/
void cuda_init_timer(CUDA_TIME_STRUCT* time)
{
    cudaEventCreate(time);
}

/***********************************************************************************************************************
* 功  能：获取系统当前时间 (CUDA版本)
* 参  数：IN/OUT:
*         time-----------------------------------当前时间
* 返回值：无
***********************************************************************************************************************/
void cuda_get_time_now(CUDA_TIME_STRUCT* time)
{
    cudaEventRecord(*time, 0);
}

/***********************************************************************************************************************
* 功  能：获取时间差 (CUDA版本)
* 参  数：IN:
*         time1----------------------------------时间1
*         time2 ---------------------------------时间2
*		 OUT:
*		  时间差（微秒）
* 返回值：时间差（微秒）
***********************************************************************************************************************/
long cuda_get_time_span_us(CUDA_TIME_STRUCT* time1, 
                           CUDA_TIME_STRUCT* time2)
{
    float elapsed_time_ms = 0.0f;

    cudaEventSynchronize(*time2);
    cudaEventElapsedTime(&elapsed_time_ms, *time1, *time2);

    return (long) (elapsed_time_ms * 1000);
}

#endif

/***********************************************************************************************************************
* 功  能：获取系统当前时间
* 参  数：IN/OUT:
*         time-----------------------------------当前时间
* 返回值：无
***********************************************************************************************************************/
void get_time_now(TIME_STRUCT* time)
{
#ifdef WIN32
    *time = clock();
#elif defined (LINUX)
    gettimeofday(time, 0);
#endif
}

/***********************************************************************************************************************
* 功  能：获取时间差
* 参  数：IN:
*         time1----------------------------------时间1
*         time2 ---------------------------------时间2
*		 OUT:
*		  时间差（微秒）
* 返回值：时间差（微秒）
***********************************************************************************************************************/
long get_time_span_us(TIME_STRUCT* time1, 
                      TIME_STRUCT* time2)
{
#ifdef WIN32
    return (long)((*time2 - *time1) * 1000);  
#elif defined (LINUX)

    return (time2->tv_sec - time1->tv_sec) * 1000000 + (time2->tv_usec - time1->tv_usec);
#endif
}